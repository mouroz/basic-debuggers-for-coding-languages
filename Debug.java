public class Debug {

	
//----------------------------------------------------------------------
//Default outputs
//----------------------------------------------------------------------
   
   	public static void out(String str) {
        System.out.println(str);
    }

    public static void out(char c) {
        System.out.println(c);
    }

    public static void out(int i) {
        System.out.println(i);
    }

    public static void out(float f) {
        System.out.println(f);
    }

    public static void out(double d) {
        System.out.println(d);
    }

    public static void out(String str, String source) {
        System.out.println(str + " from " + source);
    }

    public static void out(char c, String source) {
        System.out.println(c + " from " + source);
    }

    public static void out(int i, String source) {
        System.out.println(i + " from " + source);
    }

    public static void out(float f, String source) {
        System.out.println(f + " from " + source);
    }

    public static void out(double d, String source) {
        System.out.println(d + " from " + source);
    }

    public static void out(String str, int line) {
        System.out.println(str + " from line " + line);
    }

    public static void out(char c, int line) {
        System.out.println(c + " from line " + line);
    }

    public static void out(int i, int line) {
        System.out.println(i + " from line " + line);
    }

    public static void out(float f, int line) {
        System.out.println(f + " from line " + line);
    }

    public static void out(double d, int line) {
        System.out.println(d + " from line " + line);
    }

    public static void out(String str, String source, int line) {
        System.out.println(str + " from " + source + " from line " + line);
    }

    public static void out(char c, String source, int line) {
        System.out.println(c + " from " + source + " from line " + line);
    }

    public static void out(int i, String source, int line) {
        System.out.println(i + " from " + source + " from line " + line);
    }

    public static void out(float f, String source, int line) {
        System.out.println(f + " from " + source + " from line " + line);
    }

    public static void out(double d, String source, int line) {
        System.out.println(d + " from " + source + " from line " + line);
    }


}

